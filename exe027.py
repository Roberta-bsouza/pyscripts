import math
ang = float(input('Digite um 창ngulo qualquer: '))
sen = math.sin(math.radians(ang))
print('O 창ngulo de {} tem o Seno de {:.2f}'.format(ang, sen))
coss = math.cos(math.radians(ang))
print('O 창ngulo de {} tem o Cosseno de {:.2f}'.format(ang, coss))
tang = math.tan(math.radians(ang))
print('O 창ngulo de {} tem o Tangente de {:.2f}'.format(ang, tang))
